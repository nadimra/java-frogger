package p4_group_8_repo;

public class Highscore {

	    private int score;
	    private String name;
	    
	    public Highscore(String name, int score) {
	        this.score = score;
	        this.name = name;
	    }

	    public int getScore() {
	        return score;
	    }

	    public String getName() {
	        return name;
	    }

}

